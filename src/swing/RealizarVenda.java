    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        if(validar()){  
            carrinho.add(TPDV.addProdutoCarrinho(txtCodigo.getText(), txtCliente.getText()));
        }
        lbTotal.setText(""+TPDV.getTotal());
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private boolean validar(){
        boolean resultado = true;
        
        if(!txtCodigo.getText().isEmpty()){
            try{
                Long.parseLong(txtCodigo.getText());                 
            }catch(NumberFormatException ex){
                resultado = false;
                msn.setText("Por favor entre com um cÃ³digo vÃ¡lido!");
            }
        }
        else{
            resultado = false;
            msn.setText("Por favor entre com um cÃ³digo!");
        }             
        return resultado;
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:        
        TPDV.finalizarCompra(txtCliente.getText(), txtPagamento.getText());
        txtCliente.setText("");
        txtCodigo.setText("");
        txtPagamento.setText("");        
        carrinho.clear();
        lbFinalizada.setText("Compra realizada com sucesso! Volte sempre!");
        try {
            Thread.sleep(3000);
        } catch (InterruptedException ex) {
            Logger.getLogger(RealizarVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        lbFinalizada.setText("");
        lbTotal.setText(""+TPDV.getTotal());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if(index == -1){
            lbRemover.setText("Selecione um produto na lista para remover.");
        }
        else{
            TPDV.removeDoCarrinho(index);
            carrinho.remove(index);
        }
        lbTotal.setText(""+TPDV.getTotal());
    }//GEN-LAST:event_jButton3ActionPerformed

    
